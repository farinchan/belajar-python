# operator assignment ( = )

a = 3  # adalah assignment
print("Nilai a = ", a)

a += 5  # adalah assignment -> a = a + 5
print("Nilai a = ", a)

a -= 2  # adalah assignment -> a = a + 2
print("Nilai a = ", a)

a *= 4  # adalah assignment -> a = a * 4
print("Nilai a = ", a)

a /= 2  # adalah assignment -> a = a / 2
print("Nilai a = ", a)

a **= 2  # adalah assignment -> a = a / 2
print("Nilai a = ", a)

a //= 2  # adalah assignment -> a = a / 2
print("Nilai a = ", a)

a %= 2  # adalah assignment -> a = a / 2
print("Nilai a = ", a)


