# operasi aritmatika

a = 10
b = 5

# operasi penjulahan
hasil = a + b
print(a, " + ", b, " = ",  hasil)

# operasi pengurangan
hasil = a - b
print(a, " - ", b, " = ",  hasil)

# operasi perkalian
hasil = a * b
print(a, " * ", b, " = ",  hasil)

# operasi pembagian
# pada python hasil bagi otomatis menjadi tipe data float
hasil = a / b
print(a, " / ", b, " = ",  hasil)

# operasi modulus - sisa pembagian
hasil = a % b
print(a, " % ", b, " = ",  hasil)

# operasi floor division - dibulatkan kebawah
hasil = a // b
print(a, " // ", b, " = ",  hasil)

# operasi eksponen(pangkat)
hasil = a ** b
print(a, " ** ", b, " = ",  hasil)
